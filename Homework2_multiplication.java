public class Homework2_multiplication {
    public static void main(String[] args) {
        for (int multiplier1 = 1; multiplier1 < 10; multiplier1++){
            System.out.println();

            for (int multiplier2 = 1; multiplier2 < 10; multiplier2++){
                System.out.println(multiplier1 + " x " + multiplier2 + " = " + multiplier1 * multiplier2);
            }
        }
    }
}
